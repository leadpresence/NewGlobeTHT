package com.leadpresence.newglobetht.data.local.entity

import androidx.room.Entity
import androidx.room.PrimaryKey
import com.leadpresence.newglobetht.domain.model.Pupil

@Entity(tableName = "pupils")
data class PupilEntity(
    @PrimaryKey(autoGenerate = true)
    val id: Long = 0,
    val name: String,
    val pupilId: Long,
    val country: String,
    val image: String,
    val latitude: Double,
    val longitude: Double

)

// Mapper extensions


fun Pupil.toEntity(): PupilEntity {
    return PupilEntity(
        id = id,
        pupilId = pupilId,
        name = name,
        image = image,
        latitude = latitude,
        longitude = longitude,
        country = country,
    )
}




